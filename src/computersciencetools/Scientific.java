    private void btnResult_sciMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnResult_sciMousePressed
        screen2__sci.setText("");
        
        String answer;
        num2_sci=Double.parseDouble(screen_Sci.getText());

        if(operator_sci == "+"){
            ans_sci = num1_sci+num2_sci;
            answer=String.format("%.2f" ,ans_sci);
            screen_Sci.setText(answer);
       

        }else if(operator_sci == "-"){
            ans_sci = num1_sci-num2_sci;
            answer=String.format("%.2f" ,ans_sci);
            screen_Sci.setText(answer);
         
        }else if(operator_sci == "x" ){
            ans_sci = num1_sci*num2_sci;
            answer=String.format("%.2f" ,ans_sci);
            screen_Sci.setText(answer);
          
        }else if(operator_sci == "/"){
            ans_sci = num1_sci/num2_sci;
            answer=String.format("%.2f" ,ans_sci);
            screen_Sci.setText(answer);
           
        }else if(operator_sci == "%"){
            ans_sci = num1_sci%num2_sci;
            answer=String.format("%.2f" ,ans_sci);
            screen_Sci.setText(answer);
       
        }
    }//GEN-LAST:event_btnResult_sciMousePressed

